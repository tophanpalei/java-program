public class typecasting {
    public static void main(String[] args) {
        int a=9;
        double b=a;
        System.out.println(a);
        System.out.println(b);
        float c=5.6f;
        short  d=(short) c;
        System.out.println(d);
        char x='A';
        System.out.println(x);
        double y=-13.5d;
        long z=(long)y;
        System.out.println(z);

        float p=55.9f;
        byte q=(byte)p;
        System.out.println(q);

       
    }
}
